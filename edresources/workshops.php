          <h1 class="text-center">Curriculum Development Workshops</h1>
          <p><i>Environmental Dashboard</i> (ED)  technology is being installed in school districts in Ohio and nationally.  ED is designed to make flows of water and energy through buildings and whole communities and environmental conditions visible, engaging and teachable while also linking these to smart environmental decision-making.  A grant from the Ohio Environmental Education Fund (Ohio EPA) is supporting a series of workshops designed to better integrate this technology into the K-12 curriculum.  Workshop goals are to enhance teaching of content associated with the core curriculum and state standards, and to develop students’ “systems thinking” skills within the context of existing lessons.</p>
          <p>These workshops are a collaboration between four participating school districts and Oberlin College with support from from the Ohio Environmental Education Fund (Ohio EPA). Questions answered at <a href="mailto:dashboard@oberlin.edu">dashboard@oberlin.edu</a>.</p>
          <p>The model for these workshops assumes that teachers and curricular managers in each district are in the best position to determine precisely how ED can best be integrated into existing curricula and to develop and help assess these exercises in the context of their own districts, schools and classrooms. This  “train the trainer” model is intended to develop a cadre of experienced and enthusiastic teachers with in-depth training in the use of ED.  These workshops includes teachers from Oberlin who already have experience developing and using ED curricular materials who can serve as mentors and experts.</p>
          <h3>Curricular Development Workshops</h3>
          <p>Teachers who complete workshop #1 are eligible to apply to participate in curriculum development.  Accepted applicants will participate in a sequence of four workshops and will develop, teach and assess at least one teaching unit that uses Environmental Dashboard (ED).</p>
          <p>The overall goals for participants are to:</p>
          <ul>
            <li>Actively participate and support the learning community of participants from the four districts and Oberlin College</li>
            <li>Propose and develop an ED related lesson and an assessment plan that aligns with existing curricula and learning goals in the districts and in Ohio</li>
            <li>Develop additional knowledge and expertise related to areas of environment, decision-making and ED most relevant to the lesson proposed</li>
            <li>Teach lesson(s) developed, assess learning</li>
            <li>Present and receive feedback on lessons, revise accordingly</li>
            <li>Work with curricular specialists to modify for posting in on-line ED-related lesson repository</li>
          </ul>
          <p>The professional development sequence:</p>
          <ul>
            <li>Summer 2017: Workshops</li>
            <ul>
              <li>Date: June 13, 2017 &amp; August 1, 2017</li>
              <li>Location: Oberlin College</li>
              <li>Outcome: Goals 1-3, Draft lesson plan(s), curriculum alignment</li>
            </ul>
            <li>Fall 2017: 2 webinars</li>
            <ul>
              <li>Dates: TBD</li>
              <li>Location: Webinar</li>
            </ul>
            <li>Spring 2018: Workshop</li>
            <ul>
              <li>Date: TBD</li>
              <li>Location: Oberlin College</li>
              <li>Outcome: Share and Reflect on implementation</li>
            </ul>
          </ul>
          <p>Educators will receive a total stipend of $500 after participating in the full sequence of workshops and producing lessons and will receive reimbursement for mileage. CEU credits will provided based on completing district requirements.  Pending the success of the program and grant funding, opportunities for further curriculum development may be possible.</p>
